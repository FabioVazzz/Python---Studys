# Variables = A container for a value(strings, integers, float and booleans)

#Strings

first_name = "fabio"
print(f"You name is {first_name}")

#Integers

age = 17

print(f"You are a {age} years old")


#Float

price = 100.99

print(f"the price is {price}")

#Boolean

is_student = False

print(f"Are you a student?: {is_student}")

if is_student:
    print("You are a student")
else:
    print("You are not a student")

